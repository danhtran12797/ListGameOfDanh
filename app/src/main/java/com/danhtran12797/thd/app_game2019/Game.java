package com.danhtran12797.thd.app_game2019;

public class Game {

    private int image;
    private String name;

    public Game() {
    }

    public Game(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
